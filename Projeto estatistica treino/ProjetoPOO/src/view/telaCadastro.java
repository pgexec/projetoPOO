    //Metodo de validação do Numero de telefone inserido
    private boolean validarTelefone(String telefone)
    {
        String cleanTelefone = telefone.replace("_", "").replace(".", "").replace("-", "").replace("(","").replace(")","").trim();
        
        if (cleanTelefone.isEmpty()) 
        {
            JOptionPane.showMessageDialog(this, "O campo de CPF não pode estar vazio.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (cleanTelefone.length() != 11) 
        {    
            JOptionPane.showMessageDialog(this, "O Telefone deve ter pelo menos 11 caracteres.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
    //função que faz a validação do CPF, para impedir que a pessoa deixe vazio,
    //coloque caracteres indesejados ou não coloque o tamanho ideal
    private boolean validarCpf(String cpf)
    {
         String cleanCpf = cpf.replace("_", "").replace(".", "").replace("-", "").trim();
     
               
        if (cleanCpf.isEmpty()) 
        {
            JOptionPane.showMessageDialog(this, "O campo de CPF não pode estar vazio.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        if (cleanCpf.length() != 11) 
        {    
            JOptionPane.showMessageDialog(this, "O CPF deve ter pelo menos 14 caracteres.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (cleanCpf.matches("\\d+")) {
            return true; // O input é válido (contém apenas números)
        }  
        else 
        {
            JOptionPane.showMessageDialog(this, "Por favor, insira apenas números.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return false; // O input é inválido
        }
    }
    
    //função que faz a validação do nome da Pessoa, verificando primeiro se ela está NULL
    //depois verifica o comprimento minimo do nome,logo em seguida faz a validação se contém caracteres válidos
    private boolean validarNome(String nome) {
    
    if (nome == null || nome.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "O campo de nome não pode estar vazio.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    
    if (nome.trim().length() < 3) {
        JOptionPane.showMessageDialog(this, "O nome deve ter pelo menos 2 caracteres.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
        return false;
    }

   
    if (!nome.matches("[a-zA-Z\\s'-]+")) {
        JOptionPane.showMessageDialog(this, "O nome contém caracteres inválidos.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    return true; 
}
    
    
  
