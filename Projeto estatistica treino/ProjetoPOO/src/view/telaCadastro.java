    //BOTÃO DE CADASTRO, onde acontece toda parte do cadastro, validações dos campos, para adicionar
    // uma nova pessoa no nosso PessoaDAO
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       
 
        //Guarda as informações das caixas de entrada de dados
        boolean validado = true;
        int idade = 0;
        double renda = 0.0;
        String cidadeEscolhida = (String) objCidade.getSelectedItem();  
        String empregadoEscolha = (String) objEmpregado.getSelectedItem();
        String sexoEscolhido = (String) objSexo.getSelectedItem();
        String nomePessoa = (String) txtNome.getText();
        String cpfPessoa = (String) txtCPF.getText();
        String telefone = (String) txtTelefone.getText();
        String conheceEscolha = (String) jConheceAtingido.getSelectedItem();
        String atingidoEscolha = (String) jAtingido.getSelectedItem();
        
        
        Cidade cidade = cidadeEscolhidaNome(cidadeEscolhida);
        boolean empregado = validacaoEmpregado(empregadoEscolha);
        boolean conheceAtingidoEscolha =  validacaoAtingido(conheceEscolha);
        boolean Atingido =  validacaoAtingido(atingidoEscolha);
        
        
        
        //validação do telefone inserido
        if(!validarTelefone(telefone))
        {
            txtNome.requestFocus();
            validado = false;
        }
        
        //validação do nome inserido
         if(!validarNome(nomePessoa))
         {
             txtNome.requestFocus();
             validado = false;
         }
         
         //validação do CPF
         if(!validarCpf(cpfPessoa))
         {
             txtCPF.requestFocus();
             validado = false;
         }
         
        //validação do campo Idade para inserir o dado corretamente na pessoa
        try{
            String idadeString = (String) jIdade.getText();
             idade = Integer.parseInt(idadeString);
             if(idade < 0)
             {
                jmsgErroIdade.setText("Idade em formato ERRADO insira novamente!");
                jIdade.setText("");
                jIdade.requestFocus();
                validado = false;
             }
        }catch(NumberFormatException e)
        {
            jmsgErroIdade.setText("Idade em formato ERRADO insira novamente!");
            jIdade.setText("");
            jIdade.requestFocus();
            validado = false;
        }
        
        //validação do campo Renda para inserir o dado corretamente na pessoa
        try{
            String rendaString =  jrenda.getText().trim();
            System.out.println("Renda obtida: " + rendaString);
            renda = Double.parseDouble(rendaString);
            
            if(renda < 651.00)
            {
                jmsgErroRenda.setText("Renda em formato ERRADO insira novamente!");
                jrenda.setText("");
                jrenda.requestFocus();
                validado = false;
            }
        }catch(NumberFormatException e)
        {
            jmsgErroRenda.setText("Renda em formato ERRADO insira novamente!");
            jrenda.setText("");
            jrenda.requestFocus();
            validado = false;
        }
        
         
         //Cria um novo objeto pessoa com as informações anteriores e salva no arraylist de cadastros
         Pessoa nova = new Pessoa(nomePessoa,cpfPessoa,idade,cidade,telefone,empregado,sexoEscolhido,renda,Atingido,conheceAtingidoEscolha);
         if(validado)
         {
            JOptionPane.showMessageDialog(null, "CADASTRO CONCLUIDO!");
            cadastros.cadastrar(nova);
            telaPrincipal tlPrincipal = new telaPrincipal(cadastros);
            this.setVisible(false);
            tlPrincipal.setVisible(true);
         }       
    }//GEN-LAST:event_jButton3ActionPerformed
    
    
    
